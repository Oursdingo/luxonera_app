export function getWatchById(id: string): Watch | undefined {
  return watches.find((watch) => watch.id === id);
}

export function getWatchBySlug(slug: string): Watch | undefined {
  // Pour compatibilité, on utilise l'ID comme slug
  return watches.find((watch) => watch.id === slug || watch.slug === slug);
}

export function getFeaturedWatches(): Watch[] {
  return watches.filter((watch) => watch.featured);
}

export function getWatchesByCollection(collection: string): Watch[] {
  return watches.filter((watch) => watch.collection === collection);
}

export function getWatchesByBrand(brand: string): Watch[] {
  return watches.filter((watch) => watch.brand === brand);
}

export function getAllCollections(): string[] {
  return Array.from(new Set(watches.map((watch) => watch.collection)));
}

export function getAllBrands(): string[] {
  return Array.from(new Set(watches.map((watch) => watch.brand)));
}

export function getCollectionsByBrand(brand: string): string[] {
  const brandWatches = watches.filter((watch) => watch.brand === brand);
  return Array.from(new Set(brandWatches.map((watch) => watch.collection)));
}
